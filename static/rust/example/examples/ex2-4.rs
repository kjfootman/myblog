fn main() {

//-------------------- tuple형 변수 예제 --------------------//
    let t: (i32, usize, f32, f64) = (-1, 2, 3f32, -4.0);
    let u = (-1, 2, 3f32, -4.0);

    // tuple 변수의 개별 값 접근
    println!("t.0 * u.0 = {}", t.0 * u.0); // -1 * -1
    println!("t.1 * u.1 = {}", t.1 * u.1); // 2 * 2
    println!("t.2 * u.2 = {}", t.2 * u.2); // 3f32 * 3f32
    println!("t.3 * u.2 = {}", t.3 * u.3); // -4.0 * -4.0
}